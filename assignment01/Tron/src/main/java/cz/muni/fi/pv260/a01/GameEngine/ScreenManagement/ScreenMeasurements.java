package cz.muni.fi.pv260.a01.GameEngine.ScreenManagement;

public class ScreenMeasurements {
    private int Height;
    private int Width;

    public ScreenMeasurements(int height, int width) {
        Height = height;
        Width = width;
    }

    public int getHeight() {
        return Height;
    }

    public int getWidth() {
        return Width;
    }

}
